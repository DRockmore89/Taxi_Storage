const { Schema, model } = require('mongoose');


// to be expanded

const operatorSchema = new Schema({
  firstName: {
    type: String,
    required: true,
    trim: true,
  },
  lastName: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  },
  
});

const Operator = model('Operator', operatorSchema);

module.exports = Operator;
